// ============================================================
// models/RepairRequest.js
// Schema for customer repair/service requests
// ============================================================

const mongoose = require("mongoose");

// ─── Enums (centralized for reuse in validation) ────────────────────────────
const PROBLEM_TYPES = [
  "screen",
  "battery",
  "charging",
  "software",
  "camera",
  "speaker",
  "other",
];

const REPAIR_STATUSES = ["Received", "Checking", "Repairing", "Completed"];

const repairRequestSchema = new mongoose.Schema(
  {
    repairId: {
      type: String,
      required: true,
      unique: true,
      uppercase: true,
      trim: true,
      // Format: R1001, R1002, ... generated by utility function
      index: true, // Index for fast lookup by repairId
    },

    customerName: {
      type: String,
      required: [true, "Customer name is required"],
      trim: true,
      minlength: [2, "Name must be at least 2 characters"],
      maxlength: [80, "Name cannot exceed 80 characters"],
    },

  phoneNumber: {
  type: String,
  required: [true, "Phone number is required"],
  trim: true,
  match: [
    /^\d{10}$/,
    "Phone number must be exactly 10 digits"
  ],
},


    deviceBrand: {
      type: String,
      required: [true, "Device brand is required"],
      trim: true,
      maxlength: [50, "Device brand cannot exceed 50 characters"],
    },

    deviceModel: {
      type: String,
      required: [true, "Device model is required"],
      trim: true,
      maxlength: [80, "Device model cannot exceed 80 characters"],
    },

    problemType: {
      type: String,
      required: [true, "Problem type is required"],
      enum: {
        values: PROBLEM_TYPES,
        message: `Problem type must be one of: ${PROBLEM_TYPES.join(", ")}`,
      },
    },

    problemDescription: {
      type: String,
      required: [true, "Problem description is required"],
      trim: true,
      minlength: [10, "Description must be at least 10 characters"],
      maxlength: [1000, "Description cannot exceed 1000 characters"],
    },

    // Optional image path (stored via multer)
    image: {
      type: String,
      default: null,
    },

    status: {
      type: String,
      enum: {
        values: REPAIR_STATUSES,
        message: `Status must be one of: ${REPAIR_STATUSES.join(", ")}`,
      },
      default: "Received",
    },

    // Optional: Link repair request to a registered customer
    customer: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      default: null,
    },

    // Admin notes (internal, not visible to customers)
    adminNotes: {
      type: String,
      trim: true,
      maxlength: [500, "Admin notes cannot exceed 500 characters"],
      default: "",
    },
  },
  {
    timestamps: true, // createdAt + updatedAt auto-managed
  }
);

// ─── Virtual: Customer-facing summary object ─────────────────────────────────
repairRequestSchema.virtual("publicSummary").get(function () {
  return {
    repairId: this.repairId,
    deviceBrand: this.deviceBrand,
    deviceModel: this.deviceModel,
    problemType: this.problemType,
    status: this.status,
    createdAt: this.createdAt,
    updatedAt: this.updatedAt,
  };
});

// ─── Export constants for use in validators ──────────────────────────────────
repairRequestSchema.statics.PROBLEM_TYPES = PROBLEM_TYPES;
repairRequestSchema.statics.REPAIR_STATUSES = REPAIR_STATUSES;

module.exports = mongoose.model("RepairRequest", repairRequestSchema);